public class UserInfo {
    private String account_info;
    private String password_info;

    public String getAccount_info() {
        return account_info;
    }

    public String getPassword_info() {
        return password_info;
    }

    public void setAccount_info(String account_info) {
        this.account_info = account_info;
    }

    public void setPassword_info(String password_info) {
        this.password_info = password_info;
    }
}
